package Fundamentals;

public class Person {
    // For Constructor practices
    
    // Attributes
    String firstName;
    String lastName;
    String middleName;
    int age;
    char sex;
    
       
        Person(String firstName, String lastName, String middleName,
                int age, char sex){
            
            this.firstName = firstName;
            this.lastName = lastName;
            this.middleName = middleName;
            this.age = age;
            this.sex = sex; 
        }
        

}
